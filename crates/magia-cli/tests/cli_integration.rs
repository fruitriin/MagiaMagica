//! CLI 統合テスト (Phase 1.7 受け入れ基準)。
//!
//! ワークスペースルートの `fixtures/` を実データとして `magia` バイナリを起動する。

use std::path::PathBuf;
use std::process::Command;

use assert_cmd::prelude::*;

/// ワークスペースルートの fixtures ディレクトリ。
/// 統合テストの CWD はクレートルート (`crates/magia-cli`) なので2段上がる。
fn fixtures_dir() -> PathBuf {
    PathBuf::from(env!("CARGO_MANIFEST_DIR")).join("../../fixtures")
}

fn magia() -> Command {
    Command::cargo_bin("magia").expect("magia バイナリがビルドされている")
}

/// fixture ファイル名 → 代表関数名 (ファイル名と同名の関数を置く規約)。
const FIXTURES: &[&str] = &[
    "simple_compute",
    "io_print",
    "network_fetch",
    "db_query",
    "filesystem_read",
    "unsafe_ptr",
    "async_await",
    "result_chain",
    "match_dispatch",
    "loop_accumulate",
];

#[test]
fn render_succeeds_for_every_fixture() {
    for name in FIXTURES {
        let output = magia()
            .arg("render")
            .arg(fixtures_dir().join(format!("{name}.rs")))
            .arg("--fn")
            .arg(name)
            .output()
            .expect("CLI を起動できる");
        assert!(
            output.status.success(),
            "{name} の render が失敗: {}",
            String::from_utf8_lossy(&output.stderr)
        );
        let svg = String::from_utf8_lossy(&output.stdout);
        assert!(svg.starts_with("<!-- generated by magia-cli v"));
        assert!(svg.contains("<svg "), "{name}: SVG ルート要素がある");
        assert!(svg.trim_end().ends_with("</svg>"));
    }
}

#[test]
fn list_enumerates_all_functions() {
    let output = magia()
        .arg("list")
        .arg(fixtures_dir().join("match_dispatch.rs"))
        .output()
        .expect("CLI を起動できる");
    assert!(output.status.success());
    let stdout = String::from_utf8_lossy(&output.stdout);
    assert!(stdout.lines().any(|l| l == "match_dispatch"));
}

#[test]
fn missing_function_error_lists_candidates() {
    let output = magia()
        .arg("render")
        .arg(fixtures_dir().join("simple_compute.rs"))
        .arg("--fn")
        .arg("no_such_fn")
        .output()
        .expect("CLI を起動できる");
    assert!(!output.status.success());
    let stderr = String::from_utf8_lossy(&output.stderr);
    assert!(stderr.contains("no_such_fn"), "未発見の関数名が出る");
    assert!(
        stderr.contains("simple_compute"),
        "候補一覧が出る: {stderr}"
    );
}

#[test]
fn syntax_error_reports_line_number() {
    let dir = std::env::temp_dir().join("magia-cli-test");
    std::fs::create_dir_all(&dir).unwrap();
    let path = dir.join("broken.rs");
    std::fs::write(&path, "fn broken( {\n").unwrap();
    let output = magia()
        .arg("list")
        .arg(&path)
        .output()
        .expect("CLI を起動できる");
    assert!(!output.status.success());
    let stderr = String::from_utf8_lossy(&output.stderr);
    assert!(stderr.contains("構文エラー"));
    assert!(stderr.contains("行目"), "行番号が出る: {stderr}");
}

#[test]
fn render_syntax_error_also_reports_line_number() {
    // list と render は同じ prettify_parse_error を通る。render 経路も保証する。
    let dir = std::env::temp_dir().join("magia-cli-test");
    std::fs::create_dir_all(&dir).unwrap();
    let path = dir.join("broken2.rs");
    std::fs::write(&path, "\n\nfn broken( {\n").unwrap();
    let output = magia()
        .arg("render")
        .arg(&path)
        .arg("--fn")
        .arg("broken")
        .output()
        .expect("CLI を起動できる");
    assert!(!output.status.success());
    let stderr = String::from_utf8_lossy(&output.stderr);
    assert!(stderr.contains("構文エラー") && stderr.contains("行目"));
}

#[test]
fn non_rs_extension_warns_but_continues() {
    let dir = std::env::temp_dir().join("magia-cli-test");
    std::fs::create_dir_all(&dir).unwrap();
    let path = dir.join("sample.txt");
    std::fs::write(&path, "fn from_txt() {}\n").unwrap();
    let output = magia()
        .arg("list")
        .arg(&path)
        .output()
        .expect("CLI を起動できる");
    assert!(output.status.success(), "警告のみで処理は続行する");
    assert!(String::from_utf8_lossy(&output.stderr).contains("警告"));
    assert!(String::from_utf8_lossy(&output.stdout).contains("from_txt"));
}

#[test]
fn layers_option_filters_groups() {
    let output = magia()
        .arg("render")
        .arg(fixtures_dir().join("io_print.rs"))
        .arg("--fn")
        .arg("io_print")
        .arg("--layers")
        .arg("effects")
        .output()
        .expect("CLI を起動できる");
    assert!(output.status.success());
    let svg = String::from_utf8_lossy(&output.stdout);
    assert!(svg.contains(r#"<g class="layer-effects">"#));
    assert!(!svg.contains(r#"<g class="layer-control-flow">"#));
    assert!(!svg.contains(r#"<g class="layer-type-info">"#));
}

