#[test]
fn emit_ir_outputs_valid_json() {
    let output = magia()
        .arg("emit-ir")
        .arg(fixtures_dir().join("result_chain.rs"))
        .arg("--fn")
        .arg("result_chain")
        .output()
        .expect("CLI を起動できる");
    assert!(output.status.success());
    let json: serde_json::Value =
        serde_json::from_slice(&output.stdout).expect("emit-ir は有効な JSON を出す");
    let modules = json["modules"].as_array().expect("modules は配列");
    assert!(!modules.is_empty());
    assert!(
        modules[0]["sigils"]
            .as_array()
            .is_some_and(|s| !s.is_empty()),
        "MainRing を含む sigils が入っている"
    );
}

#[test]
fn output_flag_writes_svg_file() {
    let dir = std::env::temp_dir().join("magia-cli-test");
    std::fs::create_dir_all(&dir).unwrap();
    let out = dir.join("simple_compute.svg");
    magia()
        .arg("render")
        .arg(fixtures_dir().join("simple_compute.rs"))
        .arg("--fn")
        .arg("simple_compute")
        .arg("-o")
        .arg(&out)
        .assert()
        .success();
    let svg = std::fs::read_to_string(&out).expect("出力ファイルが存在する");
    assert!(svg.contains("</svg>"));
}

